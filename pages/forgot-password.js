import InputError from '../components/authentication/InputError'
import AuthSessionStatus from '../components/authentication/AuthSessionStatus'
import Head from 'next/head'
import ProductsHeroSecion from '../components/products/products-hero-section'
import { useAuth } from '../hooks/auth'
import { useState } from 'react'

const ForgotPassword = () => {
    const { forgotPassword } = useAuth({ middleware: 'guest' })

    const [email, setEmail] = useState('')
    const [errors, setErrors] = useState([])
    const [status, setStatus] = useState(null)

    const submitForm = event => {
        event.preventDefault()

        forgotPassword({ email, setErrors, setStatus })
    }

    return (
    <div>
        <Head>
          <title>Rest Password</title>
          <meta title="description" content="Generated by create next app" />
          <link rel="icon" href="/favicon.ico" />
        </Head>
        <ProductsHeroSecion image="/accountbg.jpg"/>
        <div dir='rtl' className="h-full lg:w-1/2 w-full px-8 py-6 mt-4"style={{"margin-top": "-100px"}}>
            <h3 className="text-right text-2xl font-Roboto font-bold">غيّر كلمة السر</h3>
            <div className="mb-4 text-sm text-gray-600">
                Forgot your password? No problem. Just let us know your
                email address and we will email you a password reset link
                that will allow you to choose a new one.
            </div>
            {/* Session Status */}
            <AuthSessionStatus className="mb-4" status={status} />

            <form className="mt-4 p-4 border border-gray-200" onSubmit={submitForm}>
                <div>
                    <label className="font-Roboto block" for="email"> البريد الإلكتروني *</label>
                    <input type="text"value={email} id="email"
                        className="w-full px-4 py-2 mt-2 border rounded-md focus:outline-none 
                        focus:ring-1 focus:ring-orange-700"
                        onChange={event => setEmail(event.target.value)}/>
                    <InputError messages={errors.email} className="mt-2" />
                </div>
                <div className="flex items-center justify-end mt-4">
                    <button className="px-6 py-2 mt-4 text-white bg-orange-500 rounded-lg hover:bg-orange-700">Email Password Reset Link</button>
                </div>
            </form>
        </div>
    </div>
    )
}

export default ForgotPassword
